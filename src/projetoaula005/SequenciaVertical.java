package projetoaula005;
public class SequenciaVertical {
    public static void main(String[] args) {
        System.out.println("Números naturais - vertical");
        for (int i = 0; i <= 10; i ++) {
            System.out.println("Número\tDobro");
            System.out.println(i + "\t" + i * 2);
        }
    }
}